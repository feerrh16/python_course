# Sistemas Numéricos: se utiliza la notación 0BaseNumero de la siguiente forma:
# Sistema Decimal
a = 10
# Binario
a = 0b1010
# Octal
a = 0o12
# Hexadecimal
a = 0xA

# Conversión de un tipo entero, incluyendo su base
# Base decimal
a = int('23', 10)
# Base binaria
a = int('10111', 2)
# Base Octal
a = int('27', 8)
# Base Hexadecimal
a = int('17', 16)
# Base 5
a = int('344', 5)
print(f'a: {a}')
